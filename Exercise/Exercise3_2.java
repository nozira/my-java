class Exercise3_2{
  public static void main(String[] args) {
    int a = 50, b = 9;

    System.out.println("a は 50\t b は 9");
    System.out.println("a と b は同じ\t" + (a == b));
    System.out.println("a は b と同じない\t" + (a != b));
    System.out.println("a は b より大きい\t" + (a > b));
    System.out.println("a は b 以上だ\t" + (a >= b));
    System.out.println("a は b より小さい\t" + (a < b));
    System.out.println("a は b 以下だ\t" + (a <= b));
  }
}
