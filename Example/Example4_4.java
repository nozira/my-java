class Example4_4{
  public static void main(String[] args) {
    int a = 3;

    switch(a){
      case 1:
        System.out.println("睦月");
        break;
      case 2:
        System.out.println("如月");
        break;
      case 3:
        System.out.println("弥生");
        break;
      default:
        System.out.println("該当する月がありません");
        break;
    }
  }
}
