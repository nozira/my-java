class Example3_3{
  public static void main(String[] args) {
    int a = 10, b = 20, c = 10;

    System.out.println(a + "==" + b + "は、" + (a == b));
    System.out.println(a + "==" + c + "は、" + (a == c));
    System.out.println(a + "!=" + b + "は、" + (a != b));
    System.out.println(a + ">" + b + "は、" + (a > b));
    System.out.println(a + ">=" + b + "は、" + (a >= b));
    System.out.println(a + "<" + b + "は、" + (a < b));
    System.out.println(a + "<=" + b + "は、" + (a <= b));
  }
}
